public class Punto {
    //Variables X y Y
    private int x;
    private int y;
//Constructor
    public Punto() {
        this.x = x;
        this.y = y;
    }
  //Getters y Setters  
    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
    
    
    
}
