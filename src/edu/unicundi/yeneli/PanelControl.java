    public void ingresarALista(){
        
        int x=0;
        int y=0;
       
        x = Integer.parseInt(this.tX.getText());
        y = Integer.parseInt(this.tY.getText());
        Punto obj = new Punto();
       obj.setX(x);
       obj.setY(y);

       listaPuntos.add(obj);

       for(int c=0; c<listaPuntos.size(); c++){
           System.out.println("++X="+ listaPuntos.get(c).getX()+ "Y="+ listaPuntos.get(c).getY());
       }
       tX.setText(null);
       tY.setText(null);
       
       contadorIngresos++;
       
       
    }
    
